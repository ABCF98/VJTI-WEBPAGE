                          <p>VJTI Covers a sixteen acre campus at a prime location at Matunga. It is easily accessible by both rail and road. The area is a hub of prestigious educational institutions including St. Joseph High School, MUICT, Don Bosco High School and Khalsa College. The Center for Industrial Research in Cotton Technology is located nearby.<br>

The present structure consists of a central administrative building around which buildings for housing the departments have been constructed from time to time. The central building consists of the main administrative offices, the library and the auditorium. The dome of the building remains the most distinctive feature of the campus.<br>

The Mechanical and Production Engineering departments are in a separate building at the north end of the campus. The Technical and Applied Chemistry department is also near that end.<br>

Hostel facilities exist for about 550 students. Block A anf B were constructed in the 1920s whereas blocks C and D were added later to accomodate the growing number of students. The hostel mess, Gymkhana and Saraswati Vachanalaya are managed by student committees.<br>
